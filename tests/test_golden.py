def test_tracker_golden_structure(
    golden_finding: Finding, golden_decision: TriageDecision, tmp_path: Path
) -> None:
    """The tracker gets a header once and one aligned row per action."""
    tracker = tmp_path / "TRACKER.md"
    tools.file_ticket(
        golden_finding,
        golden_decision,
        tickets_dir=tmp_path / "tickets",
        tracker_path=tracker,
    )
    tools.escalate(golden_finding, golden_decision, tracker_path=tracker)
    text = tracker.read_text(encoding="utf-8")

    assert text.startswith("# AutoTriage Tracker")
    assert "| Timestamp (UTC) | Finding | Verdict | Severity | Confidence" in text
    separator = "| --- | --- | --- | --- | --- | --- | --- | --- |"
    assert text.count(separator) == 1  # header separator written exactly once

    rows = [
        ln
        for ln in text.splitlines()
        if ln.startswith("| ") and "| abc123def456 |" in ln
    ]
    assert len(rows) == 2
    # The ticket row carries the recommended action; the escalate row is tagged.
    assert any("draft_pr" in r for r in rows)
    assert any("escalate:human-review" in r for r in rows)
    for row in rows:
        # Eight data columns => nine pipes on a well-formed row.
        assert row.count("|") == 9
        assert "true_positive" in row
        assert "critical" in row
        assert "0.93" in row
